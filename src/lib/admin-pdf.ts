export function downloadShortlistedDossiers(
  apps: Application[],
  jobs: Job[],
  cvByEmail: Record<string, CvProfile>,
  actor: string,
) {
  const doc = new jsPDF();
  header(
    doc,
    "Shortlisted Candidates — Bulk Dossier",
    `${apps.length} shortlisted candidate${apps.length === 1 ? "" : "s"} across ${new Set(apps.map((a) => a.jobId)).size} vacancy(ies)`,
  );

  autoTable(doc, {
    startY: 65,
    head: [["#", "Candidate", "Email", "Role", "Department", "Submitted"]],
    body: apps.map((a, i) => {
      const j = jobs.find((x) => x.id === a.jobId);
      return [i + 1, a.candidateName ?? "—", a.candidateEmail ?? "—", a.title, j?.dept ?? a.dept, a.date];
    }),
    headStyles: { fillColor: NAVY, textColor: 255, fontStyle: "bold" },
    styles: { fontSize: 8.5, cellPadding: 2.5 },
    alternateRowStyles: { fillColor: [245, 247, 250] },
  });

  apps.forEach((a, idx) => {
    doc.addPage();
    const name = a.candidateName || "Candidate";
    const job = jobs.find((j) => j.id === a.jobId);
    header(
      doc,
      `Dossier ${idx + 1} of ${apps.length} — ${name}`,
      `${a.title}${job ? ` • ${job.dept}` : ""} • Submitted ${a.date}`,
    );

    let y = 68;
    doc.setTextColor(13, 36, 84); doc.setFont("helvetica", "bold"); doc.setFontSize(11);
    doc.text("Application Summary", 12, y); y += 4;
    autoTable(doc, {
      startY: y,
      theme: "plain",
      body: [
        ["Application ID", String(a.id)],
        ["Candidate", name],
        ["Email", a.candidateEmail ?? "—"],
        ["Role applied", a.title],
        ["Department", job?.dept ?? a.dept],
        ["Salary band", job?.salaryBand ?? "—"],
        ["Visibility", job?.visibility ?? "—"],
        ["Submitted", a.date],
        ["Status", a.status],
        ["Profile completion", `${a.completion}%`],
      ],
      styles: { fontSize: 9, cellPadding: 1.5 },
      columnStyles: { 0: { fontStyle: "bold", cellWidth: 48, textColor: [90, 90, 90] } },
    });
    y = (doc as any).lastAutoTable.finalY + 6;

    const cv = a.candidateEmail ? cvByEmail[a.candidateEmail.toLowerCase()] : undefined;
    if (cv) {
      const p = cv.personal;
      doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
      doc.text("Personal Details", 12, y); y += 4;
      autoTable(doc, {
        startY: y, theme: "plain",
        body: [
          ["Full name", `${p.firstName} ${p.otherName ?? ""} ${p.lastName}`.replace(/\s+/g, " ").trim() || "—"],
          ["Date of birth", p.dob || "—"], ["Gender", p.gender || "—"],
          ["Nationality", p.nationality || "—"], ["NIN", p.nin || "—"],
          ["Phone", p.phone || "—"], ["Address", p.address || "—"],
        ],
        styles: { fontSize: 9, cellPadding: 1.5 },
        columnStyles: { 0: { fontStyle: "bold", cellWidth: 38, textColor: [90, 90, 90] } },
      });
      y = (doc as any).lastAutoTable.finalY + 6;

      if (cv.qualifications.length) {
        doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
        doc.text("Qualifications", 12, y);
        autoTable(doc, {
          startY: y + 2,
          head: [["Level", "Course / Award", "Institution", "Year"]],
          body: cv.qualifications.map((q) => [q.level, q.course || q.school || "—", q.institution || q.school || "—", q.year || "—"]),
          headStyles: { fillColor: NAVY, textColor: 255 }, styles: { fontSize: 8.5, cellPadding: 2 },
        });
        y = (doc as any).lastAutoTable.finalY + 6;
      }

      if (cv.experience.length) {
        doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
        doc.text("Work Experience", 12, y);
        autoTable(doc, {
          startY: y + 2,
          head: [["Title", "Organisation", "Start", "End", "Notes"]],
          body: cv.experience.map((e) => [e.title, e.organisation, e.start, e.end, e.description]),
          headStyles: { fillColor: NAVY, textColor: 255 }, styles: { fontSize: 8.5, cellPadding: 2 },
        });
        y = (doc as any).lastAutoTable.finalY + 6;
      }

      if (cv.skills.length) {
        doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
        doc.text("Skills", 12, y); y += 5;
        doc.setFont("helvetica", "normal"); doc.setFontSize(9); doc.setTextColor(40, 40, 40);
        doc.text(cv.skills.join(" · "), 12, y, { maxWidth: 186 });
      }
    } else {
      doc.setFont("helvetica", "italic"); doc.setFontSize(9); doc.setTextColor(120, 120, 120);
      doc.text("Full CV profile not available offline for this candidate. Open the candidate record in the HR Console to view the live dossier.", 12, y, { maxWidth: 186 });
    }
  });

  footer(doc, actor);
  doc.save(`caa-shortlist-dossiers-${Date.now()}.pdf`);
}