function openPdfInTab(doc: jsPDF, fallbackName: string) {
  try {
    const url = doc.output("bloburl") as unknown as string;
    const w = window.open(url, "_blank");
    if (!w) doc.save(fallbackName);
  } catch {
    doc.save(fallbackName);
  }
}

export function downloadCandidateCv(cv: CvProfile, app: Application | undefined, actor: string, opts?: { preview?: boolean }) {
  const doc = new jsPDF();
  const name = `${cv.personal.firstName} ${cv.personal.lastName}`.trim() || app?.candidateName || "Candidate";
  header(doc, `Candidate Dossier — ${name}`, app ? `${app.title} • Submitted ${app.date} • ${app.status}` : "CV on file");

  let y = 68;
  doc.setTextColor(13, 36, 84); doc.setFont("helvetica", "bold"); doc.setFontSize(11);
  doc.text("Personal Details", 12, y); y += 5;
  doc.setFont("helvetica", "normal"); doc.setFontSize(9); doc.setTextColor(40, 40, 40);
  const p = cv.personal;
  const personal = [
    ["Full name", `${p.firstName} ${p.otherName ?? ""} ${p.lastName}`.replace(/\s+/g, " ").trim()],
    ["Date of birth", p.dob || "—"], ["Gender", p.gender || "—"],
    ["Nationality", p.nationality || "—"], ["NIN", p.nin || "—"],
    ["Phone", p.phone || "—"], ["Email", p.email || "—"],
    ["Address", p.address || "—"],
  ];
  autoTable(doc, { startY: y, body: personal, theme: "plain", styles: { fontSize: 9, cellPadding: 1.5 }, columnStyles: { 0: { fontStyle: "bold", cellWidth: 38, textColor: [90, 90, 90] } } });
  y = (doc as any).lastAutoTable.finalY + 6;

  if (cv.qualifications.length) {
    doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
    doc.text("Qualifications", 12, y); y += 2;
    autoTable(doc, {
      startY: y + 2,
      head: [["Level", "Course / Award", "Institution", "Year"]],
      body: cv.qualifications.map((q) => [q.level, q.course || q.school || "—", q.institution || q.school || "—", q.year || "—"]),
      headStyles: { fillColor: NAVY, textColor: 255 }, styles: { fontSize: 8.5, cellPadding: 2 },
    });
    y = (doc as any).lastAutoTable.finalY + 6;
  }

  if (cv.experience.length) {
    doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
    doc.text("Work Experience", 12, y); y += 2;
    autoTable(doc, {
      startY: y + 2,
      head: [["Title", "Organisation", "Start", "End", "Notes"]],
      body: cv.experience.map((e) => [e.title, e.organisation, e.start, e.end, e.description]),
      headStyles: { fillColor: NAVY, textColor: 255 }, styles: { fontSize: 8.5, cellPadding: 2 },
    });
    y = (doc as any).lastAutoTable.finalY + 6;
  }

  if (cv.skills.length) {
    doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
    doc.text("Skills", 12, y); y += 5;
    doc.setFont("helvetica", "normal"); doc.setFontSize(9); doc.setTextColor(40, 40, 40);
    doc.text(cv.skills.join(" · "), 12, y, { maxWidth: 186 });
    y += 8;
  }

  if (cv.referees.length) {
    doc.setFont("helvetica", "bold"); doc.setFontSize(11); doc.setTextColor(13, 36, 84);
    doc.text("Referees", 12, y); y += 2;
    autoTable(doc, {
      startY: y + 2,
      head: [["Name", "Title", "Organisation", "Phone", "Email"]],
      body: cv.referees.filter((r) => r.name).map((r) => [r.name, r.title, r.organisation, r.phone, r.email]),
      headStyles: { fillColor: NAVY, textColor: 255 }, styles: { fontSize: 8.5, cellPadding: 2 },
    });
  }

  footer(doc, actor);
  const file = `caa-candidate-${name.replace(/\s+/g, "-").toLowerCase()}.pdf`;
  if (opts?.preview) openPdfInTab(doc, file); else doc.save(file);
}

export type StaffRecord = {
  empNo: string; firstName: string; lastName: string;
  dept: string; position: string; email: string; joined: string; status: string;
};

export function downloadInternsReport(apps: Application[], jobs: Job[], actor: string) {
  const doc = new jsPDF();
  const sorted = [...apps].filter((a) => a.cgpa !== undefined).sort((a, b) => (b.cgpa ?? 0) - (a.cgpa ?? 0));
  header(doc, "Intern Applications — CGPA Ranking", `${sorted.length} applications ranked by academic performance`);
  autoTable(doc, {
    startY: 65,
    head: [["Rank", "Candidate", "Email", "Position", "CGPA", "University", "Status", "Submitted"]],
    body: sorted.map((a, i) => {
      const job = jobs.find((j) => j.id === a.jobId);
      return [i + 1, a.candidateName ?? "—", a.candidateEmail ?? "—", job?.title ?? a.title, a.cgpa?.toFixed(1) ?? "—", a.university ?? "—", a.status, a.date];
    }),
    headStyles: { fillColor: NAVY, textColor: 255, fontStyle: "bold" },
    styles: { fontSize: 8, cellPadding: 2 },
    alternateRowStyles: { fillColor: [245, 247, 250] },
  });
  footer(doc, actor);
  doc.save(`caa-interns-cgpa-${Date.now()}.pdf`);
}

